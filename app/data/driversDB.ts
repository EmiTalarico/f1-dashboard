// driversDB.ts
// Base de datos de pilotos hardcodeados.
// Los campos con ? son opcionales — algunos pilotos históricos no tienen todos los datos.
// id = apellido lowercase sin acentos ni espacios. Coincide con el nombre de foto en /public/drivers/{id}.webp
// ─────────────────────────────────────────────────────────────────────────────

export type DriverProfile = {
  // Identificación
  id: string               // 'hamilton', 'schumacher', 'hakkinen'
  acronym: string          // 'HAM', 'MSC', 'HAK' — para cruzar con f1drivers.ts si es activo
  firstName: string
  lastName: string

  // Origen
  nationality: string      // en español: 'Británico', 'Alemán'
  countryCode: string      // código ISO para flagcdn: 'gb', 'de'
  birthPlace: string       // 'Stevenage, Inglaterra'
  dob: string              // 'YYYY-MM-DD'

  // Estado
  active: boolean          // true = en la grilla actual
  number?: number          // número de carrera (solo activos)

  // Carrera
  debutYear: number
  debutGP: string          // 'Gran Premio de Australia 2007'
  teams: TeamStint[]       // historial de escuderías en orden cronológico

  // Palmarés
  championships: number
  wins: number
  podiums: number
  poles: number
  fastestLaps: number
  racesEntered: number

  // Contexto
  bio: string              // párrafo corto de presentación
  status: 'active' | 'retired' | 'deceased'
}

export type TeamStint = {
  team: string             // 'Ferrari', 'McLaren'
  from: number             // año de inicio
  to: number | 'present'  // año de fin o 'present' si sigue
  color: string            // color hex del equipo para el timeline
}

